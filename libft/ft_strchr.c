#include "libft.h"

char			*ft_strchr(const char *s, int c)
{
	int		i;
	char	ch;

	i = 0;
	ch = (char)c;
	while (s[i] != '\0')
	{
		if (s[i] != ch)
			i++;
		else
			return ((char *)&s[i]);
	}
	if (s[i] == ch)
		return ((char *)&s[i]);
	return (NULL);
}
